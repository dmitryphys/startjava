package com.startjava.lesson1.variable;

public class Variable {
	public static void main(String[] args) {
		byte coreCount = 4;
		short systemType = 64;
		int frequency = 2100000000;
		long cpu = 4000000000l;
		float memorySpace = 1.7f;
		double freeMemory = 0.123;
		char systemName = 'W';
		boolean is7 = true;
		
		System.out.println(coreCount);
		System.out.println(systemType);
		System.out.println(frequency);
		System.out.println(cpu);
		System.out.println(memorySpace);
		System.out.println(freeMemory);
		System.out.println(systemName);
		System.out.println(is7);
	}
}